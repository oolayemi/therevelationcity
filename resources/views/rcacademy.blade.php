                <h4 class="heading">• Membership Class (100 Level)</h4>
                <p class="text-left"> The 100 Level class is where people are committed to membership. It is basically about discovering and committing people to Christ for salvation and to other
                    Christians in the church for membership as one family of God. To commit them to membership covenant in order to be fully assimilated from the community into the membership of the
                    church (John 3:16; 1 John 3:16). In membership class, we communicate the vision and the purposes of the church to the intending members, because membership is the way we integrate
                    them into the first stage of loyalty. At the end of this class, prospective members are expected to take small test and sign membership covenant form.
                </p>

                  <h6 class="text-bold">Benefits of membership</h6>
                  <ul>
                      <li>It identifies you as a genuine believer (Eph 2:19; Rom 12:5)</li>
                      <li>It provides a spiritual family to support and encourage you in your walk with Christ (Gal 6:1-2; Heb 10:24-25)</li>
                      <li>It gives you a place to discover and use your gifts in ministry (1 Cor. 12:4-27)</li>
                      <li>It places you under the spiritual protection of godly leaders (Heb 13:7; Acts 20:28-29)</li>
                      <li>It gives you the accountability you need to grow (Eph 5:21)</li>
                      <li>Our worship helps you focus on God. It prepares you spiritually and emotionally for the week ahead</li>
                      <li>Our fellowship helps you face life's problems by providing the support and encouragement of other Christians</li>
                      <li>Our discipleship helps you to fortify your faith by learning the truth of God's Word and applying biblical principles to your lifestyle</li>
                      <li>Our ministries help you discover and develop your talents and deploy them in serving others</li>
                      <li>Our evangelism helps you fulfill your mission of reaching your friends and family for Christ</li>
                  </ul>
                  <h6 class="text-bold">Requirements for membership</h6>
                  <ul>
                      <li>A personal profession of Christ as Lord and Savior</li>

                <div class="ftco-animate beliefs">
                    <h4 class="heading">• Maturity Class (200 Level)</h4>
                    <p class="text-left"> The 200 Level class is where people are committed to spiritual maturity. It is a class devoted to building the spiritual capacity of members by growing them
                        in Christ. It is the will of God that every believer is spiritually matured so that we all become like Christ in all forms (Eph 4:14). It is basically about discovering
                        spiritual maturity and committing to maturity covenant. It focuses on how to establish four basic habits that are necessary for growth, i.e. the habit of time with God's Word,
                        the habit of prayer, the habit of tithing and the habit of fellowship. This is the way we assimilate people into the second stage of loyalty. After this class, a small test
                        will be conducted to know how well they have understood each session. We close this class with everyone signing a maturity covenant.</p>
                    <ul>
                        <li>Spiritual growth is not automatic, it is intentional, it takes commitment and effort to grow (Phil 2:12-13)</li>
                        <li>It is not mystical; it is very practical. It requires consistent training and discipline (1 Tim 4:7)</li>
                        <li>It is not instantaneous; it is a process that takes time (Eph 4:13)</li>
                        <li>It is not a measure of what you know or believe only, it must also reflect in your behavior (James 2:18; James 3:13; Eph 5:8; Matt 7:16)</li>
                        <li>It is not a private matter, you need relationships to grow (Heb 10:24-25; 1John 1:7; 1John 3:14)</li>
                    </ul>

                    <h6 class="text-bold">Benefits of Maturity Class</h6>
                    <ul>
                        <li>It helps the members in developing the spiritual capacity</li>
                        <li>It helps in raising the commitment level of the members</li>
                        <li>It provides better understanding on the biblical principles that the church practice</li>
                        <li>It reinforces the consciousness of God in the heart of the members</li>
                        <li>It helps members to develop spiritual growth habits</li>
                    </ul>
                    <h6 class="text-bold">Requirements for membership</h6>
                    <ul>
                        <li>A personal profession of Christ as Lord and Savior</li>
                        <li>Completion of the membership class</li>
                        <li>A signed commitment to abide by the church's membership covenant</li>
                    </ul>
                </div>

                <div class="ftco-animate beliefs">
                    <h4 class="heading">• Ministry Class (300 Level):</h4>
                    <p class="text-left"> The 300 Level class is where people are committed to ministry with their various gifts, talents, and abilities. It is a class devoted to developing people to
                        serve Christ by using their gifts to minister to the needs of others in the church and outside the church (Eph 2:10; Eph 4:12). It is basically called discovering my ministry.
                        God expects every Christian to use his or her gifts and talents in ministry. We believe every member should find or create at least a ministry around their gifts that will be
                        channeled towards meeting the needs of other people in the church. We believe the church is built on four pillars of lay ministry (Rom 12:1-8).</p>
                    <ul>
                        <li>Pillar 1 - Every believer is a minister.
                            Every believer isn't a pastor, but every believer is called into ministry. Service in the body isn't optional, it is compulsory. God has drafted all of us into service,
                            there are no volunteers (Mk 10:45). Hence, we teach that every believer is created for ministry (Eph 2:10), saved for ministry (2 Tim 1:9), called into ministry
                            (1 Pet 2:9-10), gifted for ministry (1 Pet 4:10), authorized for ministry (Matt 20:26-28), to be prepared for ministry (Eph 4:11-12), needed for ministry (1 Cor. 12:27),
                            accountable for ministry, and will be rewarded for according to his or her ministry (Col 3:23-24).</li>

                        <li>Pillar 2 - Every ministry is important.
                            There are no little people in the body of Christ, and there are no insignificant ministries, every ministry is important (1 Cor. 12:18-22). Some ministries are visible,
                            and some are behind the scenes, but they are all equally valuable. Most times the perceived small ministries often make the greatest difference.</li>

                        <li>Pillar 3 - We are dependent on each other.
                            No ministry is independent of the other, every ministry is intertwined with all the others. Hence, cooperation and collaboration is required to be effectively
                            successful.</li>

                        <li>Pillar 4 - Ministry is the expression of my SHAPE.
                            Each of us was uniquely designed or shaped by God to do certain things. You are unique, wonderfully complex, a composite of many different factors. What God made you to
                            be determines what He intends for you to do. Your ministry is determined by your makeup. You must understand your shape to fulfill your ministry. When your gifts don't
                            match the role you play in life, you feel like a square peg in a round hole. The acronyms SHAPE is how God shaped you for ministry, i.e. S - Spiritual gifts, H - Heart,
                            A - Abilities, P - Personality, E - Experience.</li>
                    </ul>

                    <h6 class="text-bold">Benefits of Ministry Class</h6>
                    <ul>
                        <li>It helps people to function well in the areas of their passion</li>
                        <li>It helps to discover personality make up and ministry gifts</li>
                        <li>It helps in turning experiences into service</li>
                        <li>It helps people to harness their abilities and gifts in the areas they can be fulfilled in life and ministry</li>
                        <li>It gives people freedom to express themselves and function better in ministry</li>
                    </ul>
                    <h6 class="text-bold">Requirements/Placement Process for Ministry</h6>
                    <ul>
                        <li>Completion of the maturity and ministry class</li>
                        <li>Commitment to serving in ministry</li>
                        <li>Signing of church's ministry covenant</li>
                        <li>Completion of SHAPE profile</li>
                        <li>Have a personal interview with a ministry counselor to identify 3 or 4 possible areas of ministry</li>
                        <li>Meeting with the staff person or lay leader in charge the ministry of interest</li>
                        <li>Public commission at induction ceremony</li>
                    </ul>
